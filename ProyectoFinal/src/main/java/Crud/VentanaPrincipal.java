    private void crearestudiante() throws SQLException, ClassNotFoundException, InstantiationException, IllegalAccessException{
        
        EstudianteDAO estudiante = new EstudianteDAO();
        int identificacion = jTextField1.getText().charAt(1);
       estudiante.setId_estudiante(identificacion);
       String Nombre = jTextField2.getText();
       estudiante.setNombre(Nombre);
       String Correo = jTextField3.getText();
       estudiante.setCorreo(Correo);
       String Carrera = jComboBox1.getToolTipText();
       estudiante.setCarrera(Carrera);
       estudiante.create(estudiante);
       
       
         
    }
